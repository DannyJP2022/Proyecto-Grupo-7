    private void cargarTabla() {
        String query = "SELECT * FROM proyecto.logininfo";

        try {
            Statement statement = conexion.conexionUsuarioDB().createStatement();
            ResultSet resulset = statement.executeQuery(query);

            String[] columnas = {"id", "Nombre", "Apellido", "Nombre usuario", "Contrase√±a", "Tipo"};
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(columnas);

            int id;
            String nombre;
            String apellido;
            String username;
            String password;
            String tipo;

            while (resulset.next()) {
                
                id = resulset.getInt("id");
                nombre = resulset.getString("nombre");
                apellido = resulset.getString("apellido");
                username = resulset.getString("username");
                password = resulset.getString("password");
                tipo = resulset.getString("tipo");
                modelo.addRow(new Object[]{id, nombre, apellido, username, password, tipo});
            }
            resulset.close();
            statement.close();
            jTableUsuarios.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

